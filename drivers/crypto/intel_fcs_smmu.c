static void write_secure_reg_callback(struct stratix10_svc_client *client,
				    struct stratix10_svc_cb_data *data)
{
	struct intel_fcs_priv *priv = client->priv;

	if (data->status == BIT(SVC_STATUS_OK))
		dev_dbg(client->dev, "write to secure reg successful");
	else if (data->status == BIT(SVC_STATUS_ERROR))
		dev_dbg(client->dev, "write to secure reg failed");
	else
		dev_err(client->dev, "unhandled exception");

	complete(&priv->completion);
}

int smmu_program_reg(struct intel_fcs_priv *priv, uint32_t reg_add, uint32_t reg_value)
{
	struct stratix10_svc_chan *chan = priv->chan;
	struct stratix10_svc_client_msg msg;
	int ret;

	msg.command = COMMAND_WRITE_TO_SECURE_REG;

	msg.arg[0] = reg_add;
	msg.arg[1] = reg_value;

	priv->client.receive_cb = write_secure_reg_callback;

	ret = stratix10_svc_send(chan, &msg);

	ret = wait_for_completion_timeout(&priv->completion,
							FCS_SMMU_REQUEST_TIMEOUT);

	return ret;
}

void intel_fcs_smmu_init(struct intel_fcs_priv *priv)
{
	uint64_t ttbr_paddr_l1;
	uint64_t ttbr_paddr_l1_lower;
	uint64_t ttbr_paddr_l1_upper;
	uint64_t ttbr_paddr_l2;
	uint32_t tcr_value = 0;
	uint32_t mair_value = 0;

	tcr_value = (uint32_t) (TCR_RES1 | TCR_PS_1TiB | TCR_TG0_4KiB | TCR_IRGN0_WBA |
				TCR_T0SZ_1TiB | TCR_SH0_OUTER | TCR_ORGN0_WBA);

	mair_value = (uint32_t)GEN_MAIR_INDEX(0, MAIR_ATTR_DEVICE_nGnRnE) |
			GEN_MAIR_INDEX(1, MAIR_ATTR_DEVICE_nGnRE)  |
			GEN_MAIR_INDEX(2, MAIR_ATTR_MEMORY_IO_WBRWA);

	ttbr_paddr_l2 = virt_to_phys(l2_table);
	l1_table[0]   = SMMU_L1_TABLE_ENTRY(ttbr_paddr_l2, SMMU_UPPER_TABLE_DEFAULT);

	ttbr_paddr_l1 = virt_to_phys(l1_table);
	ttbr_paddr_l1_lower = ttbr_paddr_l1 & TRANSLATION_TABLE_LOWER_MASK;
	ttbr_paddr_l1_upper = ttbr_paddr_l1 >> TRANSLATION_TABLE_UPPER_BIT_SHIFT;

	//SSD0_REG
	smmu_program_reg(priv, SSD0_REG_ADDR, SSD0_VAL);

	//SMR8
	smmu_program_reg(priv, SMR8_REG_ADDR, SMR8_VAL);

	//S2CR8
	smmu_program_reg(priv, S2CR8_REG_ADDR, S2CR8_VAL);

	//CB8_SCTLR
	smmu_program_reg(priv, CB8_SCTRLR_REG_ADDR, CB8_SCTLR_VAL);

	//CBAR8
	smmu_program_reg(priv, CBAR8_REG_ADDR, CBAR8_VAL);

	//TCR_LPAE
	smmu_program_reg(priv, TCR_LPAE_REG_ADDR, 0x00000000);
	smmu_program_reg(priv, TCR_LPAE_REG_ADDR, tcr_value);

	//CB8_TTBR0_LOW
	smmu_program_reg(priv, CB8_TTBR0_LOW_REG_ADDR, ttbr_paddr_l1_lower);
	//CB8_TTBR0_HIGH
	smmu_program_reg(priv, CB8_TTBR0_HIGH_REG_ADDR, ttbr_paddr_l1_upper);

	//CB8_PRRR_MIR0
	smmu_program_reg(priv, CB8_PRRR_MIR0_REG_ADDR, mair_value);

	//CB8_PRRR_MIR1
	smmu_program_reg(priv, CB8_PRRR_MIR1_REG_ADDR, 0x00000000);

	//TCR2
	smmu_program_reg(priv, TCR2_REG_ADDR, TCR2_VAL);

}

void context_bank_enable(struct intel_fcs_priv *priv)
{
	//Disable translation context bank CB8_SCTLR
	smmu_program_reg(priv, CB8_SCTRLR_REG_ADDR, TRANSLATION_CONTEXT_BANK_ENABLE);
}

void context_bank_disable(struct intel_fcs_priv *priv)
{
	//Disable translation context bank CB8_SCTLR
	smmu_program_reg(priv, CB8_SCTRLR_REG_ADDR, TRANSLATION_CONTEXT_BANK_DISABLE);
}

void fill_l3_table(uint64_t phys, int l2_idx, int l3_idx)
{
	uint64_t *l3_table_ptr = 0;

	if (l3_idx == 0)
		l2_table[l2_idx] = SMMU_L2_TABLE_ENTRY(virt_to_phys(l3_tables[l2_idx]), SMMU_UPPER_TABLE_DEFAULT);

	l3_table_ptr = l3_tables[l2_idx];
	l3_table_ptr[l3_idx] = SMMU_L3_BLOCK_ENTRY(phys, (SMMU_UPPER_BLOCK_DEFAULT), SMMU_LOWER_MEMORY_SHARED_OUTER);
}

void invalidate_smmu_tlb_entries(struct intel_fcs_priv *priv)
{
	smmu_program_reg(priv, 0xFA000060, INVALIDATE_TBU);
	smmu_program_reg(priv, 0xFA000070, INVALIDATE_TBU);
}
