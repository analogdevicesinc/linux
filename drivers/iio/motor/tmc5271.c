#define TMC5271_MODE_VELPOS	1
#define TMC5271_MODE_VELNEG	2
#define TMC5271_MODE_HOLD	3

struct tmc5271_priv {
	struct spi_device	*spi;
	struct gpio_desc	*gpio_sleep;
	bool			enable;
	s32			velocity;
	u32			acceleration;
};

static int tmc5271_reg_read(struct spi_device *spi,
			    u8 reg_addr,
			    u32 *reg_data)
{
	u8 tx[5];
	u8 rx[5];
	struct spi_transfer t = {
		.tx_buf = tx,
		.rx_buf = rx,
		.len = 5,
	};
	int ret;

	tx[0] = 0x00 | (reg_addr & TMC5271_ADDRESS_MASK);
	tx[1] = 0x00;
	tx[2] = 0x00;
	tx[3] = 0x00;
	tx[4] = 0x00;

	ret = spi_sync_transfer(spi, &t, 1);

	tx[0] = 0x00 | (reg_addr & TMC5271_ADDRESS_MASK);
	tx[1] = 0x00;
	tx[2] = 0x00;
	tx[3] = 0x00;
	tx[4] = 0x00;

	ret |= spi_sync_transfer(spi, &t, 1);

	*reg_data = (rx[1] << 24) | (rx[2] << 16) |
		(rx[3] << 8) | (rx[4] << 0);

	return ret;
}

static int tmc5271_reg_write(struct spi_device *spi,
			     u8 reg_addr,
			     u32 reg_data)
{
	u8 tx[5];
	u8 rx[5];
	struct spi_transfer t = {
		.tx_buf = tx,
		.rx_buf = rx,
		.len = 5,
	};
	int ret;

	tx[0] = TMC5271_WRITE_BIT | (reg_addr & TMC5271_ADDRESS_MASK);
	tx[1] = (reg_data & 0xff000000) >> 24;
	tx[2] = (reg_data & 0x00ff0000) >> 16;
	tx[3] = (reg_data & 0x0000ff00) >> 8;
	tx[4] = (reg_data & 0x000000ff) >> 0;

	ret = spi_sync_transfer(spi, &t, 1);

	return ret;
}

static int tmc5271_reg_write_mask(struct spi_device *spi,
				 u8 reg_addr,
				 u32 mask,
				 u8 shift,
				 u32 data)
{
	u32 reg_data;
	int ret;

	ret = tmc5271_reg_read(spi, reg_addr, &reg_data);
	reg_data &= ~mask;
	reg_data |= (data << shift);
	ret |= tmc5271_reg_write(spi, reg_addr, reg_data);

	return ret;
}

static int tmc5271_en_driver(struct iio_dev *indio_dev, bool en)
{
	struct tmc5271_priv *priv = iio_priv(indio_dev);

	if (en)
		return tmc5271_reg_write_mask(priv->spi, TMC5271_REG_GCONF,
			TMC5271_DRV_ENN_MASK, TMC5271_DRV_ENN_SHIFT, 0);
	else
		return tmc5271_reg_write_mask(priv->spi, TMC5271_REG_GCONF,
			TMC5271_DRV_ENN_MASK, TMC5271_DRV_ENN_SHIFT, 1);
}

static int tmc5271_rotate(struct iio_dev *indio_dev, s32 velocity)
{
	struct tmc5271_priv *priv = iio_priv(indio_dev);
	int ret;

	ret = tmc5271_reg_write(priv->spi, TMC5271_REG_VMAX,
		abs(velocity));
	ret |= tmc5271_reg_write_mask(priv->spi, TMC5271_REG_RAMPMODE,
		TMC5271_RAMPMODE_MASK, TMC5271_RAMPMODE_SHIFT,
		(velocity >= 0) ? TMC5271_MODE_VELPOS : TMC5271_MODE_VELNEG);

	return ret;
}

static int tmc5271_setup(struct iio_dev *indio_dev)
{
	struct tmc5271_priv *priv = iio_priv(indio_dev);
	int ret;

	ret = tmc5271_en_driver(indio_dev,
			priv->enable);
	ret |= tmc5271_reg_write_mask(priv->spi, TMC5271_REG_CHOPCONF,
			TMC5271_TOFF_MASK, TMC5271_TOFF_SHIFT, 3);

	ret |= tmc5271_reg_write(priv->spi, TMC5271_REG_AMAX,
			priv->acceleration);
	ret |= tmc5271_rotate(indio_dev,
			priv->velocity);

	return ret;
}

static int tmc5271_read_raw(struct iio_dev *indio_dev,
			    struct iio_chan_spec const *chan,
			    int *val,
			    int *val2,
			    long mask)
{
	struct tmc5271_priv *priv = iio_priv(indio_dev);

	switch (mask) {
	case IIO_CHAN_INFO_ENABLE:
		*val = priv->enable;
		return IIO_VAL_INT;
	case IIO_CHAN_INFO_RAW:
		if (chan->type == IIO_VELOCITY)
			*val = priv->velocity;
		if (chan->type == IIO_ACCEL)
			*val = priv->acceleration;
		return IIO_VAL_INT;
	}

	return -EINVAL;
}

static int tmc5271_write_raw(struct iio_dev *indio_dev,
			     struct iio_chan_spec const *chan,
			     int val,
			     int val2,
			     long mask)
{
	struct tmc5271_priv *priv = iio_priv(indio_dev);

	switch (mask) {
	case IIO_CHAN_INFO_ENABLE:
		priv->enable = val;
		return tmc5271_en_driver(indio_dev, priv->enable);
	case IIO_CHAN_INFO_RAW:
		if (chan->type == IIO_VELOCITY) {
			priv->velocity = val;
			return tmc5271_rotate(indio_dev, priv->velocity);
		}
		if (chan->type == IIO_ACCEL) {
			priv->acceleration = val;
			return tmc5271_reg_write(priv->spi, TMC5271_REG_AMAX,
					priv->acceleration);
		}
	}

	return -EINVAL;
}

static int tmc5271_reg_access(struct iio_dev *indio_dev,
			      unsigned reg, unsigned writeval,
			      unsigned *readval)
{
	struct tmc5271_priv *priv = iio_priv(indio_dev);

	if (readval)
		return tmc5271_reg_read(priv->spi, reg, readval);

	return tmc5271_reg_write(priv->spi, reg, writeval);
}

static const struct iio_info tmc5271_info = {
	.read_raw = &tmc5271_read_raw,
	.write_raw = &tmc5271_write_raw,
	.debugfs_reg_access = &tmc5271_reg_access,
};

static const struct iio_chan_spec tmc5271_channels[] = {
	{
		.type = IIO_VELOCITY,
		.info_mask_separate = BIT(IIO_CHAN_INFO_RAW),
		.info_mask_shared_by_all = BIT(IIO_CHAN_INFO_ENABLE),
		.output = 1,
		.channel = 0,
	}, {
		.type = IIO_ACCEL,
		.info_mask_separate = BIT(IIO_CHAN_INFO_RAW),
		.info_mask_shared_by_all = BIT(IIO_CHAN_INFO_ENABLE),
		.output = 1,
		.channel = 1,
	}
};

static int tmc5271_probe(struct spi_device *spi)
{
	struct iio_dev *indio_dev;
	struct tmc5271_priv *priv;
	int ret;

	indio_dev = devm_iio_device_alloc(&spi->dev, sizeof(*priv));
	if (!indio_dev)
		return -ENOMEM;

	priv = iio_priv(indio_dev);

	priv->gpio_sleep = devm_gpiod_get_optional(&spi->dev, "sleep",
		GPIOD_OUT_LOW);
	if (IS_ERR(priv->gpio_sleep))
		return PTR_ERR(priv->gpio_sleep);

	if (priv->gpio_sleep) {
		gpiod_set_value_cansleep(priv->gpio_sleep, 1);
		mdelay(1);
		gpiod_set_value_cansleep(priv->gpio_sleep, 0);
		mdelay(1);
	}

	priv->spi = spi;
	priv->enable = true;
	priv->velocity = 50000;
	priv->acceleration = 100;

	ret = tmc5271_setup(indio_dev);
	if (ret)
		return ret;

	indio_dev->name = TMC5271_NAME;
	indio_dev->channels = tmc5271_channels;
	indio_dev->num_channels = ARRAY_SIZE(tmc5271_channels);
	indio_dev->info = &tmc5271_info;

	return devm_iio_device_register(&spi->dev, indio_dev);
}

static const struct of_device_id tmc5271_of_match[] = {
	{
		.compatible = "adi,tmc5271",
	},
	{ }
};
MODULE_DEVICE_TABLE(of, tmc5271_of_match);

static struct spi_driver tmc5271_driver = {
	.driver = {
		.name = TMC5271_NAME,
		.of_match_table = tmc5271_of_match,
	},
	.probe = tmc5271_probe,
};
module_spi_driver(tmc5271_driver);

