#define ADRV910X_BF_ENCODE(value, mask, shift) (((value) << (shift)) & (mask))
#define ADRV910X_BF_DECODE(value, mask, shift) (((value) & (mask)) >> (shift))
#define ADRV910X_BF_CLEAR(result, mask)  (result = ((result) & ~(mask))
#define ADRV910X_BF_UPDATE(result, value, mask, shift)  (result = ((result) & ~(mask)) | ( ((value) << (shift)) & (mask))) 
#define ADRV910X_BF_EQUAL(value, mask) ((mask) == ((value) & (mask)))

